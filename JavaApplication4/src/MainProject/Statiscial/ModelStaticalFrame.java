    private void getDaY(String s) {
         List<String> date = new ArrayList<>();
         List<String> withoutdate = new ArrayList<>();
         dataList = StatiscalModify.Search(s);
         for(OrderOff od : dataList) { 
            String date1 = getDate(od.getCreated_at());
            withoutdate.add(date1);
        }
        for(String element : withoutdate) {
            if(!day.contains(element)) {
                day.add(element);
            }
        }
        setTotal = new int[day.size()];
        for(OrderOff od : dataList) {
             String date1 = getDate(od.getCreated_at());
             for(int i = 0; i < day.size(); i++) {
                 if(date1.equals(day.get(i))) {
                     setTotal[i] += od.getTotalPrice();
                 }
             }
        }
    }

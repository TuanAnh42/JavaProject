    private String getDate(String s) {
        String[] array = s.replace("-", " ").split("\\s");
        return array[2];
    }
    
    private String getMonth(String s) {
        String[] array = s.replace("-", " ").split("\\s");
        return array[1];
    }    
    
    private void setModel(String s, String month) {
        getDaY(s);
        DefaultCategoryDataset dc = new DefaultCategoryDataset();
        for(int i = 0; i < day.size(); i++) {
            dc.setValue(setTotal[i], "Month " + month, day.get(i) +"-"+ month);
        }
        
        JFreeChart jchart = ChartFactory.createBarChart3D("", "Date", "Total Price", dc, PlotOrientation.VERTICAL, true, true, false);
        CategoryPlot plot = jchart.getCategoryPlot();
        plot.setRangeGridlinePaint(Color.red);
        
        ChartPanel chartPanel = new ChartPanel(jchart);
        MainPanel.removeAll();
        MainPanel.add(chartPanel);
        MainPanel.updateUI();
    }
